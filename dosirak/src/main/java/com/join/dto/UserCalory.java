package com.join.dto;

public class UserCalory {
	
	String userid;
	String date;
	String breakfast;
	String lunch;
	String dinner;
	String day;
	String breakfast_food;
	String lunch_food;
	String dinner_food;
	
	
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getBreakfast() {
		return breakfast;
	}
	public void setBreakfast(String breakfast) {
		this.breakfast = breakfast;
	}
	public String getLunch() {
		return lunch;
	}
	public void setLunch(String lunch) {
		this.lunch = lunch;
	}
	public String getDinner() {
		return dinner;
	}
	public void setDinner(String dinner) {
		this.dinner = dinner;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getBreakfast_food() {
		return breakfast_food;
	}
	public void setBreakfast_food(String breakfast_food) {
		this.breakfast_food = breakfast_food;
	}
	public String getLunch_food() {
		return lunch_food;
	}
	public void setLunch_food(String lunch_food) {
		this.lunch_food = lunch_food;
	}
	public String getDinner_food() {
		return dinner_food;
	}
	public void setDinner_food(String dinner_food) {
		this.dinner_food = dinner_food;
	}


}
